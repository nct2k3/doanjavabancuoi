    private void newPassKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_newPassKeyReleased
        if(newPass.getPassword().equals("") || newPass.getPassword().length < 8){
            newPass.setBorder(BorderFactory.createMatteBorder(1,1,1,1,Color.red));
            messForget.setText("Mật khẩu phải từ 8 kí tự!");
        }else{
            newPass.setBorder(BorderFactory.createEmptyBorder(1, 10, 1, 1));
            messForget.setText("");
        }
    }//GEN-LAST:event_newPassKeyReleased

    private void confirmNewPassKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_confirmNewPassKeyReleased
        String newPassF = String.valueOf(newPass.getPassword());
        String newconfirmF = String.valueOf(confirmNewPass.getPassword());
        if(!newPassF.equals(newconfirmF) || newconfirmF.length() < 8){
            confirmNewPass.setBorder(BorderFactory.createMatteBorder(1,1,1,1,Color.red));
            messForget.setText("Mật khẩu xác thực không đúng!");
        }else{
            confirmNewPass.setBorder(BorderFactory.createEmptyBorder(1, 10, 1, 1));
            messForget.setText("");
        }
    }//GEN-LAST:event_confirmNewPassKeyReleased

    private void btnCreateNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateNewActionPerformed
        String newPassF = String.valueOf(newPass.getPassword());
        String newconfirmF = String.valueOf(confirmNewPass.getPassword());
        if(!newPassF.equals("") && newPassF.length() >= 8 && newconfirmF.equals(newPassF)){
            for(account acc : listACC){
                if(acc.getGmail().equals(gmailForget.getText()) && acc.getName().equals(nameForget.getText())){
                    account newAcc = new account(acc.getId(), acc.getGmail(), acc.getName(), newPassF);
                    System.out.println(newAcc.getGmail() + newAcc.getName() + newAcc.getPass() +" "+newAcc.getId()+"");
                    ConnectToXampp.updatePassOfAccount(newAcc);
                    resetFormForget();
                    JOptionPane.showMessageDialog(rootPane, "Tạo mật khẩu thành công: " + newPassF);
                    formForget.setVisible(false);
                    formSignin.setVisible(true);
                    nameLogin.setText(acc.getName());
                    passLogin.setText(newPassF);
                    return;
                }
            }
        }
    }//GEN-LAST:event_btnCreateNewActionPerformed

