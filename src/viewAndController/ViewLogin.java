    private int checkACCSignup(String gmail, String name, String pass, String confirmPass){
        String GMAIL_REGEX = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
        boolean checkGmail = gmail.matches(GMAIL_REGEX);
        if(!gmail.equals("") && checkGmail == true && !name.equals("") && name.length() >= 6 && !pass.equals("") && pass.equals(confirmPass) && pass.length() >= 8){
            return 1;
        }
        return 0;
    }
    
