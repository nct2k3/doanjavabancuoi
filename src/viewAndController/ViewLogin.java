    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        String gmailSign = gmailSignup.getText();
        String nameSign = nameSignup.getText();
        String passSign = String.valueOf(passSignup.getPassword());
        String confirmSign = String.valueOf(confirmSignup.getPassword());
        if(gmailSignup.getText().equals("") || nameSignup.getText().equals("") || passSignup.getPassword().equals("") || confirmSignup.getPassword().equals("")){
            messSignUp.setText("Vui lòng điền đầy đủ thông tin!");
            return;
        }
        if(checkACCSignup(gmailSign, nameSign, passSign, confirmSign) == 1){
            JOptionPane.showMessageDialog(rootPane, "Đăng ký thành công tài khoản");
        }else{
            messSignUp.setText("Vui lòng kiểm tra lại thông tin!");
        }
    }//GEN-LAST:event_btnSignupActionPerformed

