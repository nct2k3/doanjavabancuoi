    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String nameLog = nameLogin.getText();
        String passLog = String.copyValueOf(passLogin.getPassword());
        if(nameLog.equals("") || passLog.equals("")){
            messLogin.setText("Vui lòng nhập đầy đủ thông tin!");
            return;
        }else if(nameLog.length() < 6 || passLog.length() < 8){
            messLogin.setText("Vui lòng kiểm tra lại thông tin!");
            return;
        }
        if(checkACCLogin(nameLog, passLog) == 1){
            messLogin.setText("");
            if(vAdmin == null){
                resetFormLogin();
                vAdmin = new ViewAdmin();
            }
            vAdmin.setVisible(true);
        }else if(checkACCLogin(nameLog, passLog) == 2){
            if(vEmployee == null){
                resetFormLogin();
                vEmployee = new ViewNhanVien();
            }
            vEmployee.setVisible(true);
        }else if(checkACCLogin(nameLog, passLog) == -1){
            messLogin.setText("Mật khẩu không đúng!");
        }else if(checkACCLogin(nameLog, passLog) == -2){
            messLogin.setText("Tên đăng nhập không tồn tại!");
        }else if(checkACCLogin(nameLog, passLog) == 0){
            messLogin.setText("Tài khoản không tồn tại!");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

